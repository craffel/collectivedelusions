import subprocess
import time
import os
import json

def wait_for_checkpoints():
    checkpoints = ["checkpoint_cifar10.pt", "checkpoint_svhn.pt", "checkpoint_fashionmnist.pt"]
    print("Waiting for checkpoints to be generated by the training job...")
    while True:
        missing = [chk for chk in checkpoints if not os.path.exists(chk)]
        if not missing:
            print("All checkpoints are ready!")
            break
        print(f"Still waiting for: {missing}. Checking again in 15 seconds...")
        time.sleep(15)

def run_merge(method, scaling):
    print(f"Running merge with method={method}, scaling={scaling}...")
    cmd = ["python", "merge.py", "--method", method, "--scaling", str(scaling)]
    try:
        result = subprocess.run(cmd, capture_output=True, text=True, check=True)
        output = result.stdout
        
        # Parse accuracy values from output
        # Format expected: Accuracy on <dataset>: <val>%
        lines = output.split('\n')
        accs = {}
        avg_acc = 0.0
        for line in lines:
            if "Accuracy on" in line:
                parts = line.split(":")
                dataset = parts[0].replace("Accuracy on", "").strip()
                val = float(parts[1].replace("%", "").strip())
                accs[dataset] = val
            if "Average Multi-task Accuracy" in line:
                parts = line.split(":")
                avg_acc = float(parts[1].replace("%", "").strip())
                
        return accs, avg_acc
    except Exception as e:
        print(f"Error running merge: {e}")
        return None, None

def main():
    wait_for_checkpoints()
    
    methods = ['task_arithmetic', 'isotropic', 'wsa']
    scalings = [0.1, 0.3, 0.5, 0.7, 0.9, 1.0]
    
    all_results = {}
    
    for method in methods:
        all_results[method] = []
        for scaling in scalings:
            accs, avg_acc = run_merge(method, scaling)
            if accs:
                entry = {
                    "scaling": scaling,
                    "cifar10": accs.get("cifar10", 0.0),
                    "svhn": accs.get("svhn", 0.0),
                    "fashionmnist": accs.get("fashionmnist", 0.0),
                    "average": avg_acc
                }
                all_results[method].append(entry)
                print(f"Result - {method} (scale {scaling}): CIFAR10={entry['cifar10']:.2f}%, SVHN={entry['svhn']:.2f}%, F-MNIST={entry['fashionmnist']:.2f}%, Avg={entry['average']:.2f}%\n")
                
    # Save all results to a JSON file
    with open("results.json", "w") as f:
        json.dump(all_results, f, indent=4)
        
    # Generate a summary markdown table
    print("\n" + "="*40 + " SUMMARY " + "="*40)
    for method in methods:
        print(f"\nMethod: {method}")
        print(f"{'Scale':<10} | {'CIFAR-10':<12} | {'SVHN':<12} | {'Fashion-MNIST':<15} | {'Average':<12}")
        print("-"*65)
        best_avg = 0.0
        best_scale = None
        for r in all_results[method]:
            print(f"{r['scaling']:<10} | {r['cifar10']:<12.2f} | {r['svhn']:<12.2f} | {r['fashionmnist']:<15.2f} | {r['average']:<12.2f}")
            if r['average'] > best_avg:
                best_avg = r['average']
                best_scale = r['scaling']
        print(f"Best: Scale {best_scale} with Average Accuracy {best_avg:.2f}%")
        
if __name__ == '__main__':
    main()
